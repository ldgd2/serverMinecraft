def _create_app_admin():
    console.print("\n[bold cyan]Creating App Administrator Account[/bold cyan]")
    username = Prompt.ask("Admin Username", default="admin")
    password = Prompt.ask("Admin Password", password=True)
    
    from database.connection import SessionLocal
    from database.models.user import User
    from app.services.auth_service import get_password_hash
    
    db = SessionLocal()
    try:
        if db.query(User).filter(User.username == username).first():
            console.print(f"[yellow]User {username} already exists.[/yellow]")
        else:
            db.add(User(username=username, hashed_password=get_password_hash(password), is_admin=True))
            db.commit()
            console.print("[bold green]✓ Admin account created.[/bold green]")
    except Exception as e:
        console.print(f"[red]Error: {e}[/red]")
        db.rollback()
    finally:
        db.close()

