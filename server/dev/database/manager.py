@app.command("restore")
def restore_backup_cmd():
    """Restore system from a backup file"""
    from app.services.backup_service import BackupService
    service = BackupService()
    
    backups = service.list_backups()
    if not backups:
        console.print("[yellow]No backups found in 'backups/' directory.[/yellow]")
        return
    
    console.print("\n[bold cyan]Select a backup to restore:[/bold cyan]")
    table = Table(show_header=True, header_style="bold yellow", box=box.SIMPLE)
    table.add_column("No.", style="dim")
    table.add_column("Filename")
    table.add_column("Size (MB)")
    table.add_column("Date")
    
    for i, b in enumerate(backups):
        size_mb = round(b['size'] / (1024 * 1024), 2)
        table.add_row(str(i+1), b['filename'], str(size_mb), b['created_at'])
    
    console.print(table)
    idx = Prompt.ask("\n❯ Enter number to restore (or 0 to cancel)", default="0")
    
    if idx == "0": return
    
    try:
        selected = backups[int(idx)-1]
        console.print(Panel(f"[bold red]DANGER: Restoring will overwrite EVERYTHING (DB, Worlds, Mods) with data from:[/bold red]\n{selected['filename']}", border_style="red"))
        confirm = Prompt.ask("Type 'RESTORE' to confirm", default="")
        
        if confirm == "RESTORE":
            if service.restore_full_backup(selected['filename']):
                console.print("[bold green]✓ System restored successfully![/bold green]")
            else:
                console.print("[bold red]✗ Restore failed.[/bold red]")
        else:
            console.print("[yellow]Restore cancelled.[/yellow]")
    except (ValueError, IndexError):
        console.print("[red]Invalid selection.[/red]")
    except Exception as e:
        console.print(f"[bold red]✗ Error during restore: {e}[/bold red]")

@app.command("roles")
def roles_cmd():
    """Manage PostgreSQL database roles (users)"""
    console.print(Panel("[bold yellow]PostgreSQL Role Management[/bold yellow]"))
    db_host = os.getenv("DB_HOST", "127.0.0.1")
    db_port = os.getenv("DB_PORT", "5432")
    super_user = Prompt.ask("Superuser (postgres)", default="postgres")
    super_pass = Prompt.ask(f"Password for {super_user}", password=True)
    
    try:
        import psycopg2
        from psycopg2 import sql
        from psycopg2.extensions import ISOLATION_LEVEL_AUTOCOMMIT
        conn = psycopg2.connect(dbname="postgres", user=super_user, password=super_pass, host=db_host, port=db_port)
        conn.set_isolation_level(ISOLATION_LEVEL_AUTOCOMMIT)
        cur = conn.cursor()
        while True:
            cur.execute("SELECT rolname FROM pg_roles WHERE rolcanlogin = true ORDER BY rolname;")
            roles = [r[0] for r in cur.fetchall()]
            console.print(f"\n[bold cyan]Roles:[/bold cyan] {', '.join(roles)}")
            console.print("[1] Create User [2] Change Password [3] Drop User [0] Back")
            sub = Prompt.ask("Action", choices=["1", "2", "3", "0"], default="0")
            if sub == "1":
                r = Prompt.ask("Username"); p = Prompt.ask("Password", password=True)
                cur.execute(sql.SQL("CREATE USER {} WITH PASSWORD {}").format(sql.Identifier(r), sql.Literal(p)))
                console.print(f"[green]User {r} created.[/green]")
            elif sub == "2":
                r = Prompt.ask("Role", choices=roles); p = Prompt.ask("New Password", password=True)
                cur.execute(sql.SQL("ALTER USER {} WITH PASSWORD {}").format(sql.Identifier(r), sql.Literal(p)))
                console.print(f"[green]Password changed for {r}.[/green]")
            elif sub == "3":
                r = Prompt.ask("Role", choices=roles)
                cur.execute(sql.SQL("DROP USER {}").format(sql.Identifier(r)))
                console.print(f"[green]User {r} dropped.[/green]")
            elif sub == "0": break
        cur.close(); conn.close()
    except Exception as e:
        console.print(f"[bold red]PG Error: {e}[/bold red]")

@app.command("fix-schema")
def fix_schema_cmd():
    """Manually add missing statistics columns to the database (Raw SQL)"""
    from database.connection import get_engine
    from sqlalchemy import text
    
    console.print(Panel("[bold yellow]MANUAL SCHEMA REPAIR[/bold yellow]"))
    console.print("[cyan]Applying raw SQL to fix missing statistics columns...[/cyan]")
    
    queries = [
        "ALTER TABLE player_accounts ADD COLUMN IF NOT EXISTS total_player_kills INTEGER DEFAULT 0;",
        "ALTER TABLE player_accounts ADD COLUMN IF NOT EXISTS total_hostile_kills INTEGER DEFAULT 0;",
        "ALTER TABLE player_accounts ADD COLUMN IF NOT EXISTS total_genocide_score INTEGER DEFAULT 0;"
    ]
    
    engine = get_engine()
    try:
        with engine.connect() as conn:
            for query in queries:
                console.print(f"Running: [dim]{query}[/dim]")
                try:
                    conn.execute(text(query))
                    conn.commit()
                except Exception as ex:
                    if "already exists" in str(ex).lower() or "duplicate column" in str(ex).lower():
                        console.print(f"[yellow]Column already exists, skipping.[/yellow]")
                    else:
                        console.print(f"[red]Error on query: {ex}[/red]")
                        
        console.print("[bold green]✓ Database schema repaired successfully.[/bold green]")
    except Exception as e:
        console.print(f"[bold red]✗ Failed to repair schema: {e}[/bold red]")

