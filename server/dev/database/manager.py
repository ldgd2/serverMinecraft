@app.command("update")
def auto_update_cmd(message: Optional[str] = typer.Option(None, "--message", "-m", help="Migration message")):
    """Auto-detect model changes, generate migration, and apply it."""
    console.print(Panel("[bold green]AUTODETECTING DATABASE CHANGES[/bold green]"))
    
    # Handle the case where message is passed as a Typer Option object instead of string
    if not isinstance(message, str):
        message = None

    if not message:
        message = Prompt.ask("Enter a brief description of the changes", default=f"auto_migration_{int(time.time())}")

    with Progress(SpinnerColumn(), TextColumn("[cyan]Analyzing models & generating migration..."), console=console) as progress:
        task = progress.add_task("gen", total=None)
        
        # 1. Try to generate autogenerated migration
        def try_generate():
            return subprocess.run(
                [sys.executable, "-m", "alembic", "revision", "--autogenerate", "-m", message],
                cwd=PROJECT_ROOT, capture_output=True, text=True
            )

        res = try_generate()
        
        if res.returncode != 0:
            err_output = (res.stderr or "") + (res.stdout or "")
            
            # AUTOMATIC CONFLICT RESOLUTION
            if "Target database is not up to date" in err_output or "Multiple heads" in err_output:
                progress.update(task, description="[yellow]Conflict detected. Cleaning and syncing DB...")
                
                # 1. Manual SQL clean if multiple heads in DB
                if "Multiple heads" in err_output:
                    from sqlalchemy import text
                    from database.connection import get_engine
                    try:
                        with get_engine().connect() as conn:
                            conn.execute(text("DELETE FROM alembic_version"))
                            conn.commit()
                    except: pass

                # 2. Sync DB to current heads
                subprocess.run([sys.executable, "-m", "alembic", "stamp", "heads"], cwd=PROJECT_ROOT)
                
                # 3. If it's still failing with "Multiple heads", it means the FILES have multiple heads.
                # Let's try to merge them automatically.
                if "Multiple heads are present" in err_output or "specify a single target revision" in err_output:
                    progress.update(task, description="[yellow]Multiple heads in files. Merging migrations...")
                    subprocess.run([sys.executable, "-m", "alembic", "merge", "heads", "-m", "merge_heads_auto"], cwd=PROJECT_ROOT)

                # Second attempt
                progress.update(task, description="[cyan]Retrying migration generation...")
                res = try_generate()

            if res.returncode != 0:
                progress.stop()
                console.print(f"[bold red]Failed to generate migration:[/bold red]\n{res.stderr or res.stdout}")
                return False

        # 2. Apply migration
        progress.update(task, description="[green]Applying changes to database...")
        success = run_migrations()
        time.sleep(1)
        
    if success:
        console.print(f"[bold green]✓ Database schema updated successfully with revision: '{message}'[/bold green]")
    else:
        console.print("[bold red]✗ Failed to apply migration to database.[/bold red]")

